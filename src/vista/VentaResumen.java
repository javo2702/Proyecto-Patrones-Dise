    void llenarTabla(ArrayList<Venta> ventas){
        DefaultTableModel dtm = (DefaultTableModel) tablaResumen.getModel();
        Object fila[] = new Object[3];
        for (Venta venta : ventas) {
            fila[0] = venta.getComprobante().getCodigo();
            fila[1] = venta.getFecha();
            fila[2] = venta.getComprobante().getMonto();
            dtm.addRow(fila);
        }
        tablaResumen.setModel(dtm);
    }

