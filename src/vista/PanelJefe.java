    public void preCargarProductos(ArrayList<Inventario> ps){
        DefaultTableModel dtmDetalles = (DefaultTableModel) tblInventario.getModel();
        Object[] campos = new Object[4];
        DecimalFormat df = new DecimalFormat("0.00");
        DecimalFormatSymbols dfs = new DecimalFormatSymbols();
        dfs.setDecimalSeparator('.');
        df.setDecimalFormatSymbols(dfs);
        for (Inventario p : ps) {
            campos[0] = ((Producto)p.getP()).getCodigo();
            campos[1] = ((Producto)p.getP()).getNombre();
            campos[2] = ((Producto)p.getP()).getPrecio_venta();
            campos[3] = p.getStock();
            dtmDetalles.addRow(campos);
        }
        tblInventario.setModel(dtmDetalles);
    }
    
    public void llenarTablaModificaciones(Inventario inv, int cantidad){
        DefaultTableModel dtmModificado = (DefaultTableModel) tblModificado.getModel();
        Object[] campos = new Object[2];
        campos[0] = ((Producto)inv.getP()).getCodigo();
        campos[1] = inv.getStock()+cantidad;
        dtmModificado.addRow(campos);
        tblModificado.setModel(dtmModificado);
    }
    
    public void eliminarInventario(){
        try {
            DefaultTableModel dtmDetalle = (DefaultTableModel) tblInventario.getModel();
            int filas=tblInventario.getRowCount();
            for (int i = 0;filas>i; i++) {
                dtmDetalle.removeRow(0);
            }
            tblInventario.setModel(dtmDetalle);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al limpiar la tabla Inventario.");
        }
    }
    public void limpiarCampos(){
        txtCodigo.setText("");
        txtStock.setText("");
        txtNuevoStock.setText("");
        tblInventario.clearSelection();
    }
