    private void tblInventarioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblInventarioMouseClicked
        // TODO add your handling code here:
        invModificado = new Inventario();
        DefaultTableModel dtmDetalle = (DefaultTableModel) tblInventario.getModel();
        indiceModificar = tblInventario.getSelectedRow();
        if(indiceModificar!=-1){
            invModificado = inventario.get(indiceModificar);
            txtCodigo.setText(((Producto)invModificado.getP()).getCodigo());
            txtStock.setText(String.valueOf(invModificado.getStock()));
        }
    }//GEN-LAST:event_tblInventarioMouseClicked

    private void btnAumentarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAumentarActionPerformed
        // TODO add your handling code here:
        cantAumentar = Integer.parseInt(JOptionPane.showInputDialog("Ingrese cantidad de stock a aumentar:"));
        txtNuevoStock.setText(String.valueOf(inventario.get(indiceModificar).getStock()+cantAumentar));
    }//GEN-LAST:event_btnAumentarActionPerformed

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        if(indiceModificar!=-1){
            AumentarImpl opDism = new AumentarImpl(inventario.get(indiceModificar),cantAumentar);
            inv.recibirOperacion(opDism);
            llenarTablaModificaciones(inventario.get(indiceModificar),cantAumentar);
        }
        limpiarCampos();
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarActionPerformed
        // TODO add your handling code here:
        int fila = tblModificado.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(null, "Seleccione un producto.");
        } else if (fila != -1) {
            DefaultTableModel dtmDetalle = (DefaultTableModel) tblModificado.getModel();
            inv.eliminarOperacion(fila);
            dtmDetalle.removeRow(fila);
        }
    }//GEN-LAST:event_btnCancelarActionPerformed

    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
        // TODO add your handling code here:
        inv.realizarOperaciones();
        limpiarCampos();
        try {
            DefaultTableModel dtmDetalle = (DefaultTableModel) tblModificado.getModel();
            int filas=tblModificado.getRowCount();
            for (int i = 0;filas>i; i++) {
                dtmDetalle.removeRow(0);
            }
            tblModificado.setModel(dtmDetalle);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al limpiar la tabla de modificaciones");
        }
        eliminarInventario();
        preCargarProductos(inventario);
    }//GEN-LAST:event_btnConfirmarActionPerformed


